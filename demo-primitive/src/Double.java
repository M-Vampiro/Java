public class Double {
    public static void main(String[] args) {

        double d1 = 0.2; // If no notation, default it is double value
        double d10 = 0.8d; // d is the notation of double
        double d2 = 0.3;
        double d3 = d1 + d2;
        System.out.println(d3); // 0.5

        double d4 = 19999.3131244213214; // double is 12 decimals

        System.out.println(d4);

        float f1 = 35.555555555555555555f; // float is 6 decimals

        System.out.println(f1);

    }
}
